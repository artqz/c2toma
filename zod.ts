const ALL = [];
for (const [i, mod] of arr.entries()) {
  const modName = modNames[i];
  const obj: Record<string, number> = {};
  for (var key of Object.keys(mod)) {
    const attr = mod[parseInt(key)];
    const sum = (100 + attr) / 100;
    obj[key] = sum;
  }
  ALL.push({ [modName]: obj });
}

